#!/bin/bash

echo "🔧 Fixing Next.js route structure..."

# Check current structure
echo "Current project structure:"
find . -name "page.tsx" -type f | grep -v node_modules | sort

echo ""
echo "Choose your project structure:"
echo "1. app/ is in root (standard)"
echo "2. app/ is inside src/"
read -p "Enter choice (1 or 2): " choice

case $choice in
  1)
    # Structure: app/ in root
    echo "Setting up app/ in root..."
    mkdir -p app/auth/signin
    mkdir -p app/auth/signup
    TARGET_DIR="app"
    ;;
  2)
    # Structure: app/ in src/
    echo "Setting up app/ in src/..."
    mkdir -p src/app/auth/signin
    mkdir -p src/app/auth/signup
    TARGET_DIR="src/app"
    ;;
  *)
    echo "Invalid choice. Defaulting to app/ in root."
    TARGET_DIR="app"
    ;;
esac

# Create basic auth pages
echo "Creating auth pages in $TARGET_DIR..."

cat > $TARGET_DIR/auth/signin/page.tsx << 'EOF'
